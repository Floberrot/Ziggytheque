    public function __construct(
        private readonly MessageBusInterface $commandBus,
        private readonly PriceCodeRepositoryInterface $repository,
    ) {
        parent::__construct();
    }

    protected function configure(): void
    {
        $this->addOption('force', null, InputOption::VALUE_NONE, 'Skip existing codes instead of aborting');
    }

    protected function execute(InputInterface $input, OutputInterface $output): int
    {
        $io = new SymfonyStyle($input, $output);
        $io->title('SLF Price Codes Import — January 2023');

        $force = $input->getOption('force');
        $imported = 0;
        $skipped = 0;

        foreach (self::PRICE_CODES as [$code, $label, $value]) {
            if ($this->repository->findByCode($code) !== null) {
                if ($force) {
                    $skipped++;
                    continue;
                }

                $io->warning(sprintf('Code "%s" already exists. Use --force to skip existing codes.', $code));
                return Command::FAILURE;
            }

            $this->commandBus->dispatch(new CreatePriceCodeCommand($code, $label, $value));
            $imported++;
        }

        $io->success(sprintf('Imported %d price codes. Skipped %d existing.', $imported, $skipped));

        return Command::SUCCESS;
    }
